public class PersonInformation {
    public static String   firstName, lastName ,address,city,state,zip,phone;

    public PersonInformation(String firstName, String lastName, String address, String city, String state, String zip, String phone) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.address = address;
        this.city = city;
        this.state = state;
        this.zip = zip;
        this.phone = phone;

    }
}
